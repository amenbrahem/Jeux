# Dimensions de la grille
LIGNES = 6
COLONNES = 7


def genere_grille():
    grille = []  # Initialize grille as an empty list
    for i in range(LIGNES):
        ligne = []  # Initialize a list for each row
        for j in range(COLONNES):
            ligne.append(0)  # Append zeros to the row list
        grille.append(ligne)  # Append the row to the grille list
    return grille

def affiche_grille(grille):
    for ligne in grille:
        print(ligne)
    print(" 1  2  3  4  5  6  7")



